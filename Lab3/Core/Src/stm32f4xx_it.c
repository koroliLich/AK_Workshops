/**
  * @brief This function handles ADC1, ADC2 and ADC3 global interrupts.
  */
void ADC_IRQHandler(void)
{
  /* USER CODE BEGIN ADC_IRQn 0 */

	uint32_t adcSum;
	uint32_t averageValue;
	uint32_t adcValue;
	float voltage;

	if (hadc1.Instance == ADC1)
	{
		adcSum = 0;

		for (int i = 0; i < 128; i++)
		{
		adcValue = HAL_ADC_GetValue(&hadc1);
		adcSum += adcValue;

		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, 0);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, 0);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, 0);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, 0);

		if (adcValue < 1024) HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, 1);
		else if (adcValue < 2047) HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, 1);
		else if (adcValue < 3000) HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, 1);
		else HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, 1);
		}

		averageValue = adcSum / 128;
		voltage = (3.3 / 4095) * averageValue;

		HAL_ADC_Start_IT(&hadc1);

	}

  /* USER CODE END ADC_IRQn 0 */
  HAL_ADC_IRQHandler(&hadc1);
  /* USER CODE BEGIN ADC_IRQn 1 */

  /* USER CODE END ADC_IRQn 1 */
}

